package day12_stringmanipulations;

public class C04_ForLoop {
    public static void main(String[] args) {
        // 1'den 100'e kadar olan sayilari yazin


        for (int i = 1; i <=100; i++) {

            System.out.print(i+ " ");

        }

        System.out.println("");//alt satra gecmek icin

        //34'en baslayarak 3'er 3'er artirarak iki basamakli sayilari
        // 100'e kadar(100 haric) sayilari yazdirin
        for (int i = 34; i <100 ; i+=3) {
            System.out.print(i+ "  ");}
        System.out.println(" ");
        for (int i = 7; i < 100; i++) {
            System.out.println(i+" ");

        }
        System.out.println(" ");







    }
}
